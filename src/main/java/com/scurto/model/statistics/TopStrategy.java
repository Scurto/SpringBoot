package com.scurto.model.statistics;

public class TopStrategy {

    private String topStrategy;

    public String getTopStrategy() {
        return topStrategy;
    }

    public void setTopStrategy(String topStrategy) {
        this.topStrategy = topStrategy;
    }

    @Override
    public String toString() {
        return "TopStrategy{" +
                "topStrategy='" + topStrategy + '\'' +
                '}';
    }
}
